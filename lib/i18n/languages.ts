export function normalizeLanguageCode(code?: string | null): AppLanguageCode {
  return (LANGUAGE_CODES as readonly string[]).includes(String(code || '').toUpperCase())
    ? (String(code).toUpperCase() as AppLanguageCode)
    : 'ENGLISH'
}

export function getLanguageHtmlSettings(code?: string | null) {
  return LANGUAGE_HTML_MAP[normalizeLanguageCode(code)] || LANGUAGE_HTML_MAP.ENGLISH
}
